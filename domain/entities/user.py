class User:
    def __init__(self, id, name, email, password, role, address, phone, documentType, documentNumber):
        self.id = id
        self.name = name
        self.email = email
        self.password = password
        self.role = role
        self.address = address
        self.phone = phone
        self.documentType = documentType
        self.documentNumber = documentNumber
